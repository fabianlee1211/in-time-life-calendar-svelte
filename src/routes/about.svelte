  <h1 class="text-digit">About This Project</h1>
  <p>
    This project is inspired by In Time and the Memento Mori Life Calendar. The
    purpose is to put our limited life into perspective. We will eventually
    time-out and die. Time is relentless, unforgiving and irreversible. But it's
    time that makes our life precious. If something is forever, we would never
    treasure it. Hopefully, this would give us a sense of urgency to make the
    best out of our life while we still can.
  </p>

  <blockquote>
    Time is free, but it's priceless. You can't own it, but you can use it. You
    can't keep it, but you can spend it. Once you've lost it you can never get
    it back. <br />- Harvey Mackay
  </blockquote>
  <h2 class="text-digit">In Time</h2>
  <img src="/images/in-time-clock.jpg" alt="In Time Clock" />
  <p>
    This is a movie back in 2011 starring Justin Timberlake and Amanda Seyfried.
    The story set stage in a far future where people are genetically engineered
    to stop aging at 25. And the lifespan timer on the forearm starts it's
    1-year countdown. Time becomes the universal currency. Everyone has to work
    for time in order to prolong their life.
  </p>
  <h2 class="text-digit">Memento Mori Life Calendar</h2>
  <img src="/images/life-calendar.png" alt="Life Calendar" />
  <a
    href="https://stoicreflections.com/products/memento-mori-life-calendar-framed"
    class="mx-auto block w-max opacity-50 -mt-4"
  >
    <em>From Stoic Reflection</em>
  </a>
  <p>
    <strong>"Memento Mori"</strong> is the latin of
    <strong>"Remember you will die"</strong>. It's a cruel but necessary message
    that reminds you how long do you have left in this life. Every square
    represents a week. Every row contains 52 weeks which represents a year. You
    would colour the squares to indicate the weeks you have lived.
  </p>
  <p>
    The calendar provides a generous assumption that we will be living up to 80
    years. According to Statista,
    <a
      href="https://www.statista.com/statistics/270861/life-expectancy-by-continent/"
    >
      the worldwide average human life expectancy is 73 years
    </a>. The stats varies across different continents. Perhaps with further
    technological and medical advancement, we might live even longer but there
    will always be a cap.
  </p>
  <h2 class="text-digit">How to use it?</h2>
  <p>
    The timer is formatted as it was in the movie. And the calendar works
    exactly the same as the handwritten one.
  </p>
  <img src="/images/timer-explanation.png" alt="Timer Explanation" />
  <p>
    The timer and the digitalized life calendar will be filled automatically
    once you provide a birth date. The squares will be updated every minute so
    you don't have to refresh the page to see the changes.
  </p>
  <p>
    Whenever you start complaing, resenting, procrastinating or whatever that's
    halting yourself, come and have a glimpse on how much time you are letting
    go. Ask yourself is it worth letting it tick away? Could I have made more
    use of it?
  </p>
  <blockquote>
    Determine never to be idle. No person will have occasion to complain of the
    want of time who never loses any. It is wonderful how much can be done if we
    are always doing. <br />- Thomas Jefferson
  </blockquote>
